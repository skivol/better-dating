CREATE TABLE country (
	id uuid NOT NULL,
	"name" varchar(255) NOT NULL,
	CONSTRAINT country_pk PRIMARY KEY (id)
);

CREATE TABLE region (
	id uuid NOT NULL,
	"name" varchar(255) NOT NULL,
	country_id uuid NOT NULL,
	CONSTRAINT region_pk PRIMARY KEY (id),
	CONSTRAINT region_fk FOREIGN KEY (country_id) REFERENCES country(id)
);

CREATE TABLE populated_locality (
	id uuid NOT NULL,
	"name" varchar(255) NOT NULL,
	region_id uuid NOT NULL,
	CONSTRAINT populated_locality_pk PRIMARY KEY (id),
	CONSTRAINT populated_locality_fk FOREIGN KEY (region_id) REFERENCES region(id)
);

CREATE TABLE place (
	id uuid NOT NULL,
	"name" varchar(255) NOT NULL,
	latitude numeric(9,6) NOT NULL,
	longitude numeric(9,6) NOT NULL,
	CONSTRAINT place_pk PRIMARY KEY (id)
);

